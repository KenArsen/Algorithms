def find_single_number(nums):
    """
    Находит число, которое встречается один раз в массиве, где каждое другое число встречается дважды.

    :param nums: Список чисел (целые числа).
    :return: Число, которое встречается один раз.
    """
    # Используем set для получения уникальных чисел и умножаем их сумму на 2
    unique_sum = sum(set(nums)) * 2

    # Вычисляем сумму всех чисел в исходном списке
    total_sum = sum(nums)

    # Разница между удвоенной суммой уникальных чисел и суммой всех чисел дает искомое число
    single_number = unique_sum - total_sum

    return single_number


# Пример использования функции
nums = [4, 1, 2, 1, 2]
single_number = find_single_number(nums)
print(f"Число, встречающееся один раз: {single_number}")
