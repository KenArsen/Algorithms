def calculate_fibonacci_number(n):
    """
    Вычисляет n-е число Фибоначчи с использованием формулы Бине.

    Формула Бине для числа Фибоначчи:
    F(n) = (1 / sqrt(5)) * (((1 + sqrt(5)) / 2) ** n - ((1 - sqrt(5)) / 2) ** n)

    :param n: Порядковый номер числа Фибоначчи (целое число).
    :return: n-е число Фибоначчи.
    """
    import math

    # Вычисление корня из 5
    sqrt_5 = math.sqrt(5)

    # Вычисление первого компонента формулы Бине
    phi = (1 + sqrt_5) / 2

    # Вычисление второго компонента формулы Бине
    psi = (1 - sqrt_5) / 2

    # Применение формулы Бине для вычисления n-го числа Фибоначчи
    f_n = (1 / sqrt_5) * (phi ** n - psi ** n)

    return round(f_n)  # Округление до ближайшего целого числа


# Пример использования функции
n = int(input("Введите число: "))
fibonacci_number = calculate_fibonacci_number(n)
print(f"{n}-е число Фибоначчи: {fibonacci_number}")
